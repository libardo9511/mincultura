    private void tblArtistasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblArtistasMouseClicked
        // TODO add your handling code here:
        if (!artistas.isEmpty()) {
            btnDetalles.setEnabled(true);
            artistaSeleccionado = artistas.get(tblArtistas.getSelectedRow());
        }
    }//GEN-LAST:event_tblArtistasMouseClicked

    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        // TODO add your handling code here:
        if (artistaSeleccionado != null) {
            try {
                this.pinturas = ctrPintura.listarPinturasPorArtista(artistaSeleccionado.getCodigo());
                String informacion = "Pinturas: " + artistaSeleccionado.getNombre() +
                        " " + artistaSeleccionado.getApellido() + "\n\n";
                for (Pintura pintura : pinturas) {
                    informacion += "Código: " + pintura.getCodigo() + "\n";
                    informacion += "Precio: " + pintura.getPrecio() + "\n\n";
                }
                JOptionPane.showMessageDialog(rootPane, informacion,
                        "Información Obras", 1);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error: " + ex.getMessage(),
                        "Error: " + ex.getClass().getSimpleName(), 0);
            }
        }
    }//GEN-LAST:event_btnDetallesActionPerformed


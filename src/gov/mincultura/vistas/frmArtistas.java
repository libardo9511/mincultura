    private ArrayList<Artista> listarArtistas() {
        ArrayList<Artista> obtenerArtistas = null;
        try {
            obtenerArtistas = ctrArtista.listarArtistas();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + ex.getMessage(),
                    "Error: " + ex.getClass().getSimpleName(), 0);
        }
        return obtenerArtistas;
    }

    private void actualizarTabla() {
        this.modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int vColIndex) {
                return false;
            }

        };
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("Código");
        columnas.add("Nombre");
        columnas.add("Apellido");

        columnas.forEach(columna -> {
            modelo.addColumn(columna);
        });
        this.tblArtistas.setModel(modelo);
        for (Artista artista : artistas) {
            Object[] rowData = new Object[]{artista.getCodigo(), artista.getNombre(),
                artista.getApellido()};
            modelo.addRow(rowData);
        }
        this.tblArtistas.setModel(modelo);
    }


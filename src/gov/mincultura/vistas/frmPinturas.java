    private ArrayList<Pintura> listarPinturas() {
        ArrayList<Pintura> obtenerPinturas = null;
        try {
            obtenerPinturas = ctrPintura.listarPinturas();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + ex.getMessage(),
                    "Error: " + ex.getClass().getSimpleName(), 0);
        }
        return obtenerPinturas;
    }

    private void actualizarTabla() {
        this.modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int vColIndex) {
                return false;
            }

        };
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("Código");
        columnas.add("Precio");
        columnas.add("Fecha");
        columnas.add("Artista");

        columnas.forEach(columna -> {
            modelo.addColumn(columna);
        });
        this.tblPinturas.setModel(modelo);
        for (Pintura pintura : pinturas) {
            Object[] rowData = new Object[]{pintura.getCodigo(), pintura.getPrecio(),
                pintura.getFecha(),
                pintura.getArtista().getNombre() + " " + pintura.getArtista().getApellido()};
            modelo.addRow(rowData);
        }
        this.tblPinturas.setModel(modelo);
    }


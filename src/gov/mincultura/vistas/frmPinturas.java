    private void tblPinturasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPinturasMouseClicked
        // TODO add your handling code here:
        if (!pinturas.isEmpty()) {
            btnDetalles.setEnabled(true);
            pinturaSeleccionada = pinturas.get(tblPinturas.getSelectedRow());
        }
    }//GEN-LAST:event_tblPinturasMouseClicked

    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        // TODO add your handling code here:
        if (pinturaSeleccionada != null) {
            String informacion = "";
            informacion += "Pintura: " + pinturaSeleccionada.getCodigo() + "\n";
            informacion += "Código artista: " + pinturaSeleccionada.getArtista().getCodigo() + "\n";
            informacion += "Artista: " + pinturaSeleccionada.getArtista().getNombre() + " ";
            informacion += pinturaSeleccionada.getArtista().getApellido() + "\n";

            JOptionPane.showMessageDialog(rootPane, informacion,
                    "Información Pintura", 1);
        }
    }//GEN-LAST:event_btnDetallesActionPerformed

